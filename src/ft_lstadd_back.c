#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *node)
{
	t_list	*tmp;

	if (!lst)
		return ;
	if (!(*lst))
	{
		*lst = node;
		return ;
	}
	tmp = *lst;
	while (tmp->next)
		tmp = tmp->next;
	tmp->next = node;
}
